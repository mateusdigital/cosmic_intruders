#pragma once

// Headers
#include <memory> // Smart Pointers ;D

//----------------------------------------------------------------------------//
// Shared Pointer                                                             //
//----------------------------------------------------------------------------//
#define COOPER_SHARED_PTR_OF(_type_) \
    typedef std::shared_ptr<_type_> SPtr


//----------------------------------------------------------------------------//
// Unique Pointer                                                             //
//----------------------------------------------------------------------------//
#define COOPER_UNIQUE_PTR_OF(_type_) \
    typedef std::unique_ptr<_type_> UPtr


//----------------------------------------------------------------------------//
// All Smart Pointers.                                                        //
//----------------------------------------------------------------------------//
#define COOPER_SMART_PTRS_OF(_type_) \
    COOPER_SHARED_PTR_OF(_type_);    \
    COOPER_UNIQUE_PTR_OF(_type_)
